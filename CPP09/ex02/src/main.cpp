#include "../include/PmergeMe.hpp"

static bool	validDigits(int argc, char **argv)
{
	for (int i = 0; i < argc -1; i++)
	{
		std::string arg = argv[i];
		for (size_t j = 0; j < arg.size(); j++)
		{
			if (!isdigit(arg[j]))
				return (false);
		}
	}
	return (true);
}

int main(int argc, char **argv)
{
	++argv;
	if (argc < 2)
	{
		std::cout << RED << "Error: invalid number of arguments" << RESET << std::endl;
		return (1);
	}
	else if (!validDigits(argc, argv))
	{
		std::cout << RED << "Error: arguments are not digits" << RESET << std::endl;
		return (1);
	}

	PmergeMe pmergeMe(argc - 1, argv);
	pmergeMe.splitContainer();
	return (0);
}