#include "../include/ClapTrap.hpp"

static	void	printingInfos(const ClapTrap& toPrint)
{
	for (int i = 0; i < 64; i++)
		std::cout << "_";
	std::cout << "\n\n";
	std::cout << "Current status: " << std::endl;
	std::cout << "Name: " << toPrint.getName() << " | " << "HP: " << toPrint.getHitP() << " | " 
		<< "Energy: " << toPrint.getEnergyP() << " | " << "Attack Damage: " << toPrint.getAttackD() << std::endl;
	for (int i = 0; i < 64; i++)
		std::cout << "_";
	std::cout << "\n";
}

int	main(void)
{
	// Criating constructors
	ClapTrap	jes("Jesibel");
	ClapTrap	gla("Glaydson");
	std::cout << "\n";


	//Instancing attributes
	jes.setAttackD(10);
	gla.setAttackD(7);
	std::cout << "\n";

	//Attacking
	jes.attack(gla.getName());
	gla.takeDamage(jes.getAttackD());
	gla.attack(jes.getName());

	std::cout << "\n";
	jes.setEnergyP(0);
	jes.attack("idosa");

	//first print
	printingInfos(jes);
	printingInfos(gla);
	return (0);
}