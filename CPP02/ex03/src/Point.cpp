#include "../include/Point.hpp"

Point::Point() : x(0), y(0){
}

Point::Point(const int xValue, const int yValue) : x(xValue), y(yValue){
}

Point::Point(const float xValue, const float yValue) : x(xValue), y (yValue){
}

Point::~Point(){
}

Point::Point(const Point& toCopy)
{
	*this = toCopy;
}

Point& Point::operator=(const Point& toCopy)
{
	if (this != &toCopy)
	{
		(Fixed&)this->x = toCopy.x;
		(Fixed&)this->y = toCopy.y;
	}
	return (*this);
}

Fixed	Point::getX(void) const
{
	return (this->x);
}

Fixed	Point::getY(void) const
{
	return (this->y);
}