#include "../include/Serializer.hpp"

int	main(void)
{
	Data		obj;
	uintptr_t	ptr;

	obj.number = 42;
	obj.text = "original";
	obj.isOkay = true;
	obj.value = 0.0f;

	std::cout << obj << std::endl;
	ptr = Serializer::serialize(&obj);	

	Data *newObj = Serializer::deserialize(ptr);
	newObj->text = "deserialized";
	std::cout << *newObj;	
	return (0);
}