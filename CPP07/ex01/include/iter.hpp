#ifndef ITER_HPP
# define ITER_HPP

#include <iostream>
#define RED	"\033[0;31m"
#define BYELLOW	"\033[1;33m"
#define YELLOW	"\033[0;33m"
#define BBLUE	"\033[1;34m"
#define BWHITE	"\033[1;37m"
#define BLACK	"\033[1;30m"
#define GREEN	"\033[0;32m"
#define RESET	"\033[0m"

template <typename T>
void	iter(T* array, size_t length, void(*func)(T&))
{
	for (size_t i = 0; i < length; i++)
		func(array[i]);
}

template <typename T>
void	iter(T const* array, size_t length, void(*func)(T const&))
{
	for (size_t i = 0; i < length; i++)
		func(array[i]);
}

template <typename T>
void	increment(T& num)
{
	std::cout << YELLOW << "Incrementing to: " << GREEN << ++num << RESET << std::endl;
}

template <typename T>
void	decrement(T& num)
{
	std::cout << YELLOW << "Decrementing to: " << GREEN << --num << RESET << std::endl;
}

template <typename T>
void	printing(T& num)
{
	std::cout << YELLOW << "Printing: " << GREEN << num << RESET << RESET << std::endl;
}

#endif
