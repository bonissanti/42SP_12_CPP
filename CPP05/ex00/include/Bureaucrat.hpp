#ifndef BUREAUCRAT_HPP
# define BUREAUCRAT_HPP

#include <iostream>
#include <exception>
#define BRED	"\033[0;31m"
#define BYELLOW	"\033[1;33m"
#define BBLUE	"\033[1;34m"
#define BLACK	"\033[1;30m"
#define BGREEN	"\033[0;32m"
#define RESET	"\033[0m"

class	Bureaucrat
{
	private:
	const std::string name;
	int			grade;

	public:
	Bureaucrat();
	Bureaucrat(std::string name, int grade);
	~Bureaucrat();
	Bureaucrat(const Bureaucrat& toCopy);
	Bureaucrat& operator=(const Bureaucrat& toCopy);
	std::string getName(void) const;
	int getGrade(void) const;

	Bureaucrat&	operator++(int);
	Bureaucrat&	operator--(int);

	class	GradeTooHighException : public std::exception
	{
		public:
		char const *what() const throw(){
				return (BRED"Error: Grade too high. Max possible is 0"RESET);
		}
	};

	class	GradeTooLowException : public std::exception
	{
		public:
		char const *what() const throw(){
			return (BRED"Error: Grade too low. Min possible is 150"RESET);
		}
	};
};

std::ostream&	operator<<(std::ostream& os, const Bureaucrat& toPrint);

#endif
